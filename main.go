package main

import (
	"fmt"
)

func main() {
	fmt.Println("hello everyone")
	// Работа с числами
	fmt.Println("1+1 =", 1+1)
	fmt.Println("5.0/2.0 =", 5.0/2.0)
	// Логические выражения
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	// Работа с текстов
	fmt.Println("go" + "lang")

}
